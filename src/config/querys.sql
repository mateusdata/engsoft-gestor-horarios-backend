CREATE TABLE usuarios (
    matricula VARCHAR(20) PRIMARY KEY,
    nome VARCHAR(255),
    email VARCHAR(255) UNIQUE,
    senha VARCHAR(255),
    departamento VARCHAR(50),
    cargo VARCHAR(50),
    token VARCHAR(255),
    expiracao_token TIMESTAMP,
    administrador BOOLEAN DEFAULT FALSE,
    codigo VARCHAR(6)
);

CREATE TABLE disciplinas (
    cod VARCHAR(45) PRIMARY KEY,
    nome VARCHAR(45),
    professor_um VARCHAR(20), 
    professor_dois VARCHAR(45),
    turma VARCHAR(45),
    semana VARCHAR(10),
    horas VARCHAR(10),
    FOREIGN KEY (professor_um) REFERENCES usuarios(matricula)
);


